import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader, Dataset

# Define the dataset class
class BinaryClassificationDataset(Dataset):
    def __init__(self, X, y):
        self.X = X
        self.y = y

    def __len__(self):
        return len(self.y)

    def __getitem__(self, idx):
        return self.X[idx], self.y[idx]

# Define the model
class BinaryClassificationModel(nn.Module):
    def __init__(self, input_dim, hidden_dim, output_dim):
        super(BinaryClassificationModel, self).__init__()
        self.fc1 = nn.Linear(input_dim, hidden_dim)
        self.fc2 = nn.Linear(hidden_dim, output_dim)
        self.relu = nn.ReLU()
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        out = self.fc1(x)
        out = self.relu(out)
        out = self.fc2(out)
        out = self.sigmoid(out)
        return out

# Define the hyperparameters
input_dim = 10
hidden_dim = 5
output_dim = 1
lr = 0.001
num_epochs = 100

# Create the dataset
X = torch.randn(100, input_dim)
y = torch.randint(low=0, high=2, size=(100, 1)).float()
dataset = BinaryClassificationDataset(X, y)

# Create the data loader
batch_size = 10
data_loader = DataLoader(dataset, batch_size=batch_size, shuffle=True)

# Create the model and the loss function
model = BinaryClassificationModel(input_dim, hidden_dim, output_dim)
criterion = nn.BCELoss()

# Create the optimizer
optimizer = optim.SGD(model.parameters(), lr=lr)

# Train the model
for epoch in range(num_epochs):
    for batch_X, batch_y in data_loader:
        # Forward pass
        outputs = model(batch_X)
        loss = criterion(outputs, batch_y)

        # Backward and optimize
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

    if (epoch+1) % 10 == 0:
        print('Epoch [{}/{}], Loss: {:.4f}'.format(epoch+1, num_epochs, loss.item()))

# Test the model
with torch.no_grad():
    X_test = torch.randn(10, input_dim)
    y_test = torch.randint(low=0, high=2, size=(10, 1)).float()
    outputs = model(X_test)
    predicted = (outputs > 0.5).float()
    accuracy = (predicted == y_test).float().mean()
    print('Test Accuracy: {:.4f}'.format(accuracy.item()))

